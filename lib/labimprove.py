"""labimprove — stage-only candidate generator + safety tests (SI-027, SI-028).

Per roadmap section 23 / SI-027 + SI-028, this module stages candidate
skill changes to ``improvement/candidates/<id>/`` (gitignored) and runs
safety tests on them. **Candidates are never applied to live skills.**
Promotion to a live skill is a human action (SI-029, future).

The two public entry points are:

  - ``stage_candidate(...)`` — write a candidate's patch, metadata, and
    rollback artifact to ``improvement/candidates/<id>/``. Returns the
    candidate metadata dict. Does NOT apply the patch to the live
    skill. The candidate stays staged until a human approves it.

  - ``run_safety_tests(candidate_id, ...)`` — run the SI-028 safety
    test suite on a staged candidate. Returns
    ``{"passed": bool, "tests": [...], "hard_failure": bool}``. A single
    hard_failure = instant rejection (the candidate cannot be
    promoted, regardless of any other quality signal).

Design rules (per roadmap section 14 + ADR-0001):

  - **Default-deny mutation.** The allowlist
    (``improvement/policy/mutation-allowlist.yaml``) lists every file a
    candidate MAY modify. Everything else is denied. ``scope/SKILL.md``
    and all TCB paths are explicitly excluded. The allowlist itself is
    TCB — the candidate may read it but never modify it.

  - **Stage-only.** ``stage_candidate`` writes to
    ``improvement/candidates/<id>/`` (gitignored per SI-000). It never
    touches the live skill. The live skill is only modified by a
    human-approved promotion (SI-029, future).

  - **Reverse diff as rollback.** For every staged candidate, we also
    write ``rollback.patch`` — the reverse unified diff that would
    undo the change. This is generated by computing the diff in the
    opposite direction (swap ``+`` and ``-`` lines). It's a
    best-effort artifact; the canonical rollback is "git checkout" on
    the live skill, but the reverse diff is useful for review and for
    restoring a copy in a non-git context.

  - **No network, no subprocess.** This module is pure Python — it
    reads files, writes files, and parses diffs. It never spawns a
    subprocess and never touches the network. This makes it safe to
    run inside the evaluation sandbox (ADR-0003) and easy to test.

  - **Provenance is mandatory.** Every staged candidate carries a
    ``provenance.json`` with session, agent, model, timestamp. This is
    for auditability — every change to a skill must be traceable to a
    specific run, agent, and model.

Safety tests (SI-028):

  1. **MUT-001** Patch only modifies allowlisted files (uses
     ``mutation_check.validate_candidate_patch``).
  2. **MUT-002** Patch does not touch any safety-critical path
     (``denied_safety_critical`` in the allowlist). This is a subset
     of #1 but is checked separately so a safety-critical violation is
     reported with its own reason.
  3. **SIZE-001** Patch does not increase cyclomatic complexity beyond
     the ceiling (``complexity_ceiling: 15`` in
     ``improvement/config/optimization.yaml``). The ceiling is the
     maximum DELTA, not the absolute value.
  4. **SIZE-002** Patch does not exceed ``max_candidate_diff_size``
     (50000 chars).
  5. **SIZE-003** Modified skill does not exceed ``max_skill_length``
     (50000 chars after patch).
  6. **LEAK-001** Patch does not contain any string from
     ``evals/**/private/labels.json`` or ``improvement/private/`` (if
     those files exist). A patch that contains a private label string
     is strong evidence the candidate read private evaluation data.

Tests #1 and #2 are hard-failure (safety-critical). Tests #3–#5 are
hard-failure (budget/complexity). Test #6 is hard-failure (leakage).
A single hard-failure = instant rejection.

This module is TCB (per ADR-0001). The candidate may read it but never
modify it.
"""

