def _load_optimization_config(config_path: Path) -> dict[str, int]:
    """Load optimization config, returning the relevant limits.

    Returns a dict with keys ``complexity_ceiling``, ``max_diff_size``,
    ``max_skill_length``. Missing keys fall back to the module defaults.
    Missing file → all defaults.
    """
    out = {
        "complexity_ceiling": _DEFAULT_COMPLEXITY_CEILING,
        "max_diff_size": _DEFAULT_MAX_DIFF_SIZE,
        "max_skill_length": _DEFAULT_MAX_SKILL_LENGTH,
    }
    if not config_path.is_file():
        return out
    try:
        data = yaml.safe_load(config_path.read_text(encoding="utf-8")) or {}
    except yaml.YAMLError:
        return out
    opt = data.get("optimization") if isinstance(data, dict) else None
    if not isinstance(opt, dict):
        return out
    for k in out:
        v = opt.get(k)
        if isinstance(v, int | float):
            out[k] = int(v)
    return out


def _cyclomatic_complexity(text: str) -> int:
    """Estimate cyclomatic complexity of a Markdown/text file.

    For Markdown skill files, true cyclomatic complexity (in the
    McCabe sense) doesn't apply — there's no control flow graph. We
    use a proxy: the count of decision-point keywords that indicate
    branching logic in the skill's instructions. These are the
    keywords an agent following the skill would treat as a branch:

      - ``if``, ``unless``, ``when``, ``while``, ``for``
      - ``case``, ``switch`` (rare in prose, but present in code blocks)
      - ``try``, ``except``, ``catch`` (in embedded code blocks)

    This is a rough proxy but it's good enough to catch a candidate
    that adds a giant branching decision tree to a skill. The ceiling
    is on the DELTA (increase), not the absolute value, so a skill
    that's already complex doesn't fail just for being complex — it
    fails only if the candidate makes it MORE complex.

    The count is case-insensitive, word-bounded (``\b``) so "if" inside
    a word like "device" doesn't count.
    """
    pattern = re.compile(
        r"\b(if|unless|when|while|for|case|switch|try|except|catch)\b",
        re.IGNORECASE,
    )
    return len(pattern.findall(text))


def _collect_private_label_strings(repo_root: Path) -> set[str]:
    """Collect strings that are private evaluation labels.

    Scans:
      - ``evals/**/private/labels.json`` (private eval labels, gitignored)
      - ``improvement/private/`` (real outcomes, gitignored)

    Returns a set of strings. For JSON files, we recursively walk the
    structure and collect all string values (these are the labels
    themselves — case IDs, expected verdicts, etc.). For YAML files,
    same. For other files, we read line by line and collect any
    non-trivial string (>= 4 chars, alphanumeric).

    A candidate patch that contains ANY of these strings is presumed
    to have read private data. This is defense-in-depth: the
    evaluation sandbox already blocks file reads outside the
    candidate's scratch dir (ADR-0003), but this catches accidents.

    Returns an empty set if no private dirs exist (e.g. on a fresh
    install that hasn't been populated). The leakage test is then a
    no-op (vacuously passes).
    """
    out: set[str] = set()

    # evals/**/private/labels.json
    for p in repo_root.glob("evals/**/private/labels.json"):
        try:
            data = json.loads(p.read_text(encoding="utf-8"))
        except (OSError, json.JSONDecodeError):
            continue
        _collect_strings(data, out)

    # improvement/private/** — walk all files.
    priv = repo_root / "improvement" / "private"
    if priv.is_dir():
        for p in priv.rglob("*"):
            if not p.is_file():
                continue
            try:
                text = p.read_text(encoding="utf-8")
            except (OSError, UnicodeDecodeError):
                continue
            # Try JSON first, then YAML, then line-by-line.
            try:
                data = json.loads(text)
                _collect_strings(data, out)
                continue
            except (OSError, json.JSONDecodeError):
                pass
            try:
                data = yaml.safe_load(text)
                if isinstance(data, dict | list):
                    _collect_strings(data, out)
                    continue
            except (OSError, yaml.YAMLError):
                pass
            # Line-by-line: collect non-trivial strings.
            for line in text.splitlines():
                s = line.strip()
                if len(s) >= 4 and re.search(r"[A-Za-z0-9]", s):
                    out.add(s)

    return out


def _collect_strings(obj: Any, out: set[str]) -> None:
    """Recursively walk a JSON/YAML structure, collecting string values."""
    if isinstance(obj, str):
        if len(obj) >= 4 and re.search(r"[A-Za-z0-9]", obj):
            out.add(obj)
    elif isinstance(obj, dict):
        for v in obj.values():
            _collect_strings(v, out)
    elif isinstance(obj, list):
        for v in obj:
            _collect_strings(v, out)


def _check_leakage(patch: str, label_strings: set[str]) -> tuple[bool, str]:
    """Check if the patch contains any private label string.

    Returns ``(is_leak, detail)``. If ``is_leak`` is True, ``detail`` is
    the first matched string (truncated for the report). If False,
    ``detail`` is the empty string.

    We check each label string as a substring of the patch (not as a
    whole line) — a candidate that embeds a private label in a comment
    or a variable name still leaks it. We require the label to be at
    least 4 chars to avoid false positives on common short strings.
    """
    for s in label_strings:
        if s in patch:
            return True, s[:80]
    return False, ""


def run_safety_tests(
    candidate_id: str,
    candidates_dir: Path | None = None,
    allowlist_path: Path | None = None,
    config_path: Path | None = None,
    repo_root: Path | None = None,
) -> dict:
    """Run safety tests on a staged candidate.

    Args:
        candidate_id: the candidate UUID (matches the directory name
            under ``candidates_dir``).
        candidates_dir: override the candidates directory (for
            testing). Defaults to ``<repo>/improvement/candidates/``.
        allowlist_path: override the allowlist path (for testing).
            Defaults to ``<repo>/improvement/policy/mutation-allowlist.yaml``.
        config_path: override the optimization config path (for
            testing). Defaults to ``<repo>/improvement/config/optimization.yaml``.
        repo_root: override the repo root (for testing). Defaults to
            the repo containing the allowlist.

    Returns:
        ``{"passed": bool, "tests": [...], "hard_failure": bool}``.

        ``passed`` is True iff every test passed. ``hard_failure`` is
        True iff any safety-critical test failed (MUT-001, MUT-002,
        LEAK-001, or any SIZE test). When ``hard_failure`` is True,
        ``passed`` is False — a hard failure cannot be a pass.

        Each entry in ``tests`` is::

            {
              "name": "MUT-001",
              "description": "patch only modifies allowlisted files",
              "passed": bool,
              "detail": "...",     # human-readable explanation
              "hard_failure": bool # does this test's failure = hard failure?
            }
    """
    # Resolve paths.
    if allowlist_path is None:
        # Find the repo root from candidates_dir if given, else cwd.
        if candidates_dir is not None:
            repo_root = repo_root or _repo_root_from(Path(candidates_dir))
        else:
            repo_root = repo_root or _repo_root_from(Path.cwd())
        allowlist_path = Path(repo_root) / DEFAULT_ALLOWLIST_REL
        config_path = config_path or (Path(repo_root) / DEFAULT_CONFIG_REL)
        if candidates_dir is None:
            candidates_dir = Path(repo_root) / CANDIDATES_DIR
    else:
        allowlist_path = Path(allowlist_path)
        if repo_root is None:
            repo_root = _repo_root_from(allowlist_path)
        if config_path is None:
            config_path = Path(repo_root) / DEFAULT_CONFIG_REL
        if candidates_dir is None:
            candidates_dir = Path(repo_root) / CANDIDATES_DIR

    candidates_dir = Path(candidates_dir)
    candidate_dir = candidates_dir / candidate_id

    tests: list[dict] = []

    def _record(
        name: str, desc: str, passed: bool, detail: str, hard_failure: bool
    ) -> None:
        tests.append({
            "name": name,
            "description": desc,
            "passed": passed,
            "detail": detail,
            "hard_failure": hard_failure,
        })

    # Check the candidate directory exists.
    if not candidate_dir.is_dir():
        _record("SETUP", "candidate directory exists", False,
                f"candidate dir not found: {candidate_dir}", hard_failure=True)
        return {
            "passed": False,
            "tests": tests,
            "hard_failure": True,
        }

    patch_path = candidate_dir / "skill.patch"
    metadata_path = candidate_dir / "metadata.json"

    if not patch_path.is_file():
        _record("SETUP", "skill.patch exists", False,
                f"skill.patch not found in {candidate_dir}", hard_failure=True)
        return {"passed": False, "tests": tests, "hard_failure": True}

    patch_text = patch_path.read_text(encoding="utf-8")

    # Load metadata to get the skill_path.
    skill_path_str: str | None = None
    if metadata_path.is_file():
        try:
            metadata = json.loads(metadata_path.read_text(encoding="utf-8"))
            skill_path_str = metadata.get("skill_path")
        except (OSError, json.JSONDecodeError):
            pass
    if not skill_path_str:
        # Fall back to parsing the patch for the modified path.
        parsed = MC.parse_patch_paths(patch_text)
        if parsed:
            skill_path_str = parsed[0]["path"]

    # ── Load config limits ──────────────────────────────────────────────────────
    cfg = _load_optimization_config(Path(config_path) if config_path else Path("/nonexistent"))

    # ════════════════════════════════════════════════════════════════════════════
    # TEST MUT-001: patch only modifies allowlisted files
    # ════════════════════════════════════════════════════════════════════════════
    try:
        violations = MC.validate_candidate_patch(allowlist_path, patch_path)
        if violations:
            paths = [v["path"] for v in violations]
            _record("MUT-001", "patch only modifies allowlisted files", False,
                    f"non-allowlisted modifications: {paths}", hard_failure=True)
        else:
            _record("MUT-001", "patch only modifies allowlisted files", True,
                    "all modified paths are in the allowlist", hard_failure=True)
    except Exception as e:  # noqa: BLE001 — we want to catch any parser failure
        _record("MUT-001", "patch only modifies allowlisted files", False,
                f"allowlist check error: {e}", hard_failure=True)

    # ════════════════════════════════════════════════════════════════════════════
    # TEST MUT-002: patch does not touch safety-critical paths
    # (separate from MUT-001 so a safety violation gets its own reason)
    # ════════════════════════════════════════════════════════════════════════════
    safety_violations: list[dict] = []
    try:
        for entry in MC.parse_patch_paths(patch_text):
            allowed, reason = MC.check_mutation(allowlist_path, Path(entry["path"]))
            if not allowed and reason.startswith("safety-critical:"):
                safety_violations.append({"path": entry["path"], "reason": reason})
    except Exception as e:  # noqa: BLE001
        _record("MUT-002", "patch does not touch safety-critical paths", False,
                f"parse error: {e}", hard_failure=True)
    else:
        if safety_violations:
            _record("MUT-002", "patch does not touch safety-critical paths", False,
                    f"safety-critical modifications: {safety_violations}", hard_failure=True)
        else:
            _record("MUT-002", "patch does not touch safety-critical paths", True,
                    "no safety-critical paths touched", hard_failure=True)

    # ════════════════════════════════════════════════════════════════════════════
    # TEST SIZE-002: diff size under limit
    # (do this before SIZE-001/SIZE-003 so a huge patch fails fast)
    # ════════════════════════════════════════════════════════════════════════════
    diff_size = len(patch_text)
    if diff_size > cfg["max_diff_size"]:
        _record("SIZE-002", f"diff size under limit ({cfg['max_diff_size']} chars)", False,
                f"diff is {diff_size} chars (>{cfg['max_diff_size']})", hard_failure=True)
    else:
        _record("SIZE-002", f"diff size under limit ({cfg['max_diff_size']} chars)", True,
                f"diff is {diff_size} chars", hard_failure=True)

    # ════════════════════════════════════════════════════════════════════════════
    # TEST SIZE-003: skill length under limit
    # ════════════════════════════════════════════════════════════════════════════
    skill_text_after: str | None = None
    skill_len_detail = ""
    if skill_path_str:
        repo_root_path = Path(repo_root) if repo_root else _repo_root_from(allowlist_path)
        live_skill = repo_root_path / skill_path_str
        if live_skill.is_file():
            try:
                base_text = live_skill.read_text(encoding="utf-8")
                skill_text_after = _apply_patch_to_text(base_text, patch_text)
            except Exception as e:  # noqa: BLE001
                skill_len_detail = f"could not apply patch: {e}"
        else:
            # The skill file doesn't exist live — this could be a new
            # skill (creation). Treat the patch-added lines as the
            # skill content.
            added_lines = [ln[1:] for ln in patch_text.splitlines() if ln.startswith("+")
                           and not ln.startswith("+++")]
            if added_lines:
                skill_text_after = "\n".join(added_lines)
            else:
                skill_len_detail = "skill file not found and no added lines in patch"
    else:
        skill_len_detail = "no skill_path in metadata and no path in patch"

    if skill_text_after is not None:
        skill_len = len(skill_text_after)
        limit = cfg["max_skill_length"]
        desc = f"skill length under limit ({limit} chars)"
        if skill_len > limit:
            _record("SIZE-003", desc, False,
                    f"skill is {skill_len} chars after patch (>{limit})",
                    hard_failure=True)
        else:
            _record("SIZE-003", desc, True,
                    f"skill is {skill_len} chars after patch", hard_failure=True)
    else:
        limit = cfg["max_skill_length"]
        _record("SIZE-003", f"skill length under limit ({limit} chars)", False,
                f"could not measure skill length: {skill_len_detail}", hard_failure=True)

    # ════════════════════════════════════════════════════════════════════════════
    # TEST SIZE-001: complexity under ceiling (delta)
    # ════════════════════════════════════════════════════════════════════════════
    if skill_path_str and skill_text_after is not None:
        repo_root_path = Path(repo_root) if repo_root else _repo_root_from(allowlist_path)
        live_skill = repo_root_path / skill_path_str
        if live_skill.is_file():
            base_text = live_skill.read_text(encoding="utf-8")
            base_c = _cyclomatic_complexity(base_text)
            after_c = _cyclomatic_complexity(skill_text_after)
            delta = after_c - base_c
            if delta > cfg["complexity_ceiling"]:
                _record("SIZE-001",
                        f"complexity under ceiling (delta <= {cfg['complexity_ceiling']})", False,
                        f"complexity delta = +{delta} (>{cfg['complexity_ceiling']}) "
                        f"[base={base_c}, after={after_c}]",
                        hard_failure=True)
            else:
                _record("SIZE-001",
                        f"complexity under ceiling (delta <= {cfg['complexity_ceiling']})", True,
                        f"complexity delta = {delta:+d} [base={base_c}, after={after_c}]",
                        hard_failure=True)
        else:
            # New skill — absolute complexity is the metric.
            after_c = _cyclomatic_complexity(skill_text_after)
            ceil = cfg["complexity_ceiling"]
            new_desc = f"complexity under ceiling (<= {ceil} for new skill)"
            if after_c > ceil:
                _record("SIZE-001", new_desc, False,
                        f"new skill complexity = {after_c} (>{ceil})",
                        hard_failure=True)
            else:
                _record("SIZE-001", new_desc, True,
                        f"new skill complexity = {after_c}", hard_failure=True)
    else:
        _record("SIZE-001", "complexity under ceiling", False,
                "could not measure complexity (skill length test also failed)",
                hard_failure=True)

    # ════════════════════════════════════════════════════════════════════════════
    # TEST LEAK-001: no private label access
    # ════════════════════════════════════════════════════════════════════════════
    repo_root_path = Path(repo_root) if repo_root else _repo_root_from(allowlist_path)
    label_strings = _collect_private_label_strings(repo_root_path)
    if not label_strings:
        # No private labels exist — vacuously passes.
        _record("LEAK-001", "no private label access", True,
                "no private label files found (vacuous pass)", hard_failure=True)
    else:
        is_leak, detail = _check_leakage(patch_text, label_strings)
        if is_leak:
            _record("LEAK-001", "no private label access", False,
                    f"patch contains private label string: {detail!r}", hard_failure=True)
        else:
            _record("LEAK-001", "no private label access", True,
                    f"patch contains none of {len(label_strings)} private label strings",
                    hard_failure=True)

    # ─── Aggregate ─────────────────────────────────────────────────────────────
    all_passed = all(t["passed"] for t in tests)
    any_hard_fail = any(not t["passed"] and t["hard_failure"] for t in tests)

    result = {
        "passed": all_passed,
        "tests": tests,
        "hard_failure": any_hard_fail,
    }

    # Write the safety-checklist with results.
    checklist_path = candidate_dir / "safety-checklist.md"
    try:
        checklist_lines = [
            "# Safety checklist — candidate " + candidate_id,
            "",
            f"Overall passed: {all_passed}",
            f"Hard failure: {any_hard_fail}",
            "",
            "## Test results",
            "",
        ]
        for t in tests:
            mark = "[x]" if t["passed"] else "[ ]"
            checklist_lines.append(f"- {mark} {t['name']}: {t['description']}")
            if not t["passed"]:
                checklist_lines.append(f"    - {t['detail']}")
        checklist_lines.append("")
        checklist_lines.append(f"Status: {'PASS' if all_passed else 'FAIL'}"
                               f"{' (HARD FAILURE)' if any_hard_fail else ''}")
        checklist_path.write_text("\n".join(checklist_lines) + "\n", encoding="utf-8")
    except OSError:
        # Best-effort write — don't fail the test suite on a write error.
        pass

    return result


