# ─── Constants ─────────────────────────────────────────────────────────────────

CANDIDATES_DIR = "improvement/candidates"  # gitignored per SI-000 / .gitignore

# Repo-relative paths to TCB files (used by run_safety_tests to find
# the allowlist and config without needing a repo-root argument).
DEFAULT_ALLOWLIST_REL = "improvement/policy/mutation-allowlist.yaml"
DEFAULT_CONFIG_REL = "improvement/config/optimization.yaml"

# Default safety-test limits (used only if the config file is missing
# or a key is absent — defense-in-depth so tests don't crash on a
# fresh install that hasn't been fully configured).
_DEFAULT_COMPLEXITY_CEILING = 15
_DEFAULT_MAX_DIFF_SIZE = 50000
_DEFAULT_MAX_SKILL_LENGTH = 50000


# ─── Helpers ───────────────────────────────────────────────────────────────────


def _now_iso() -> str:
    """Current UTC time as ISO 8601 with ``Z`` suffix (e.g. 2026-07-19T12:34:56Z)."""
    return datetime.now(tz=UTC).strftime("%Y-%m-%dT%H:%M:%SZ")


def _sha256_text(text: str) -> str:
    """Hex sha256 of a string (UTF-8 encoded)."""
    return hashlib.sha256(text.encode("utf-8")).hexdigest()


def _repo_root_from(anchor: Path) -> Path:
    """Find the repo root from an anchor path.

    Walks up from ``anchor`` looking for ``improvement/policy/`` (which
    exists in every install of the lab). Falls back to ``anchor``'s
    parents until it finds one that contains
    ``improvement/policy/mutation-allowlist.yaml``.
    """
    anchor = anchor.resolve()
    for cand in [anchor, *anchor.parents]:
        if (cand / DEFAULT_ALLOWLIST_REL).is_file():
            return cand
    # Fall back to the anchor itself — callers will get a FileNotFoundError
    # when they try to load the allowlist, which is a clear signal.
    return anchor


def _apply_patch_to_text(text: str, patch: str) -> str:
    """Apply a unified diff to text and return the result.

    This is a minimal unified-diff applier. It handles the common case
    of a single-hunk patch against a known base. It is NOT a full
    ``patch(1)`` implementation — it does not handle:

      - context line fuzz (lines that match with whitespace differences)
      - offset adjustment (hunks applied at a different line than stated)
      - multiple files in one patch (callers apply one file at a time)
      - binary patches

    For the safety tests, we only need an approximate "what would the
    skill look like after this patch" to measure its length. A
    precise apply is the job of ``git apply`` at promotion time
    (SI-029), not at staging time.

    Strategy: parse hunks (``@@ -start,len +start,len @@``), and for
    each hunk, replace the lines that start with ``-`` (removed) with
    the lines that start with ``+`` (added), keeping context lines
    (leading space) as-is. We use the hunk's ``-start`` to find the
    insertion point in the current text.

    Raises ValueError if the patch is malformed or the context lines
    don't match the base text at the stated offset. The caller (safety
    tests) treats this as a failed apply — the skill-length test
    reports "could not apply patch" and the candidate fails.
    """
    lines = text.splitlines(keepends=False)
    out = list(lines)
    offset_shift = 0  # accumulated line shift from earlier hunks

    hunks: list[tuple[int, int, list[str]]] = []  # (old_start, old_len, body_lines)
    cur_hunk: tuple[int, int, list[str]] | None = None

    for line in patch.splitlines():
        if line.startswith("@@"):
            if cur_hunk is not None:
                hunks.append(cur_hunk)
            m = re.match(r"^@@ -(\d+)(?:,(\d+))? \+(\d+)(?:,(\d+))? @@", line)
            if not m:
                raise ValueError(f"malformed hunk header: {line!r}")
            old_start = int(m.group(1))
            old_len = int(m.group(2)) if m.group(2) is not None else 1
            cur_hunk = (old_start, old_len, [])
        elif cur_hunk is not None and (
            line.startswith(" ") or line.startswith("+") or line.startswith("-")
            or line == ""
        ):
            # Empty lines within a hunk are context lines (a leading
            # space would be present in a strict diff, but some tools
            # omit it for empty context lines).
            cur_hunk[2].append(line)
        elif line.startswith(("---", "+++", "diff ", "index ")):
            # File header — ignore. We already know which file this is.
            continue
        else:
            # Unknown line — end the current hunk.
            if cur_hunk is not None:
                hunks.append(cur_hunk)
                cur_hunk = None

    if cur_hunk is not None:
        hunks.append(cur_hunk)

    for old_start, old_len, body in hunks:
        # Separate body into removed, added, context.
        removed: list[str] = []
        added: list[str] = []
        context: list[str] = []
        for bline in body:
            if bline.startswith("-"):
                removed.append(bline[1:])
            elif bline.startswith("+"):
                added.append(bline[1:])
            else:
                # Context line (leading space stripped) or empty line.
                ctx = bline[1:] if bline.startswith(" ") else bline
                context.append(ctx)
                removed.append(ctx)  # context is in both old and new
                added.append(ctx)

        # Find the insertion point. old_start is 1-based; 0 means "prepend
        # before line 1" (used for file creation).
        start_idx = 0 if old_start == 0 else old_start - 1 + offset_shift

        # Verify the context matches (best-effort — if old_len > 0 we
        # expect the removed lines to match the base at start_idx).
        # Check at least the first removed line matches (when there
        # are removed lines; pure-add hunks have old_len = 0).
        if old_len > 0 and 0 <= start_idx < len(out) and removed and removed[0] != out[start_idx]:
            # Context mismatch — try to find the right offset by
            # searching nearby. This is a very small fuzz tolerance.
            found = -1
            for delta in range(-5, 6):
                idx = start_idx + delta
                if 0 <= idx < len(out) and removed[0] == out[idx]:
                    found = idx
                    break
            if found < 0:
                raise ValueError(
                    f"context mismatch at line {old_start}: "
                    f"expected {removed[0]!r}, got {out[start_idx]!r}"
                )
            start_idx = found

        # Replace the range [start_idx, start_idx + old_len) with added.
        end_idx = start_idx + old_len
        out[start_idx:end_idx] = added
        offset_shift += len(added) - old_len

    return "\n".join(out)


def _reverse_patch(patch: str) -> str:
    """Produce a reverse unified diff (swap + and - lines).

    This is a best-effort rollback artifact. For a unified diff::

        --- a/PATH
        +++ b/PATH
        @@ -1,3 +1,3 @@
         context
        -old
        +new
         context

    The reverse is::

        --- a/PATH
        +++ b/PATH
        @@ -1,3 +1,3 @@
         context
        -new
        +old
         context

    Hunk headers are rewritten to swap the ``-`` and ``+`` ranges. File
    headers (``--- a/PATH`` / ``+++ b/PATH``) are swapped. ``diff --git``
    lines are preserved (they're symmetric).

    Lines that start with ``+`` become ``-`` and vice versa. Context
    lines (leading space) are preserved. Empty lines are preserved.
    """
    out: list[str] = []
    for line in patch.splitlines():
        if line.startswith("@@"):
            m = re.match(r"^@@ -(\d+)(?:,(\d+))? \+(\d+)(?:,(\d+))? @@", line)
            if m:
                old_s, old_l, new_s, new_l = m.groups()
                old_l = old_l or "1"
                new_l = new_l or "1"
                out.append(f"@@ -{new_s},{new_l} +{old_s},{old_l} @@")
                continue
            out.append(line)
        elif line.startswith("+++") or line.startswith("---"):
            # Swap +++ and ---.
            if line.startswith("+++"):
                out.append("-" + line[1:])
            else:
                out.append("+" + line[1:])
        elif line.startswith("+"):
            out.append("-" + line[1:])
        elif line.startswith("-"):
            out.append("+" + line[1:])
        else:
            out.append(line)
    return "\n".join(out)


