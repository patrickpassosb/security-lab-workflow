def stage_candidate(
    skill_path: Path,
    patch: str,
    linked_lessons: list[str],
    evaluation_summary: str = "",
    provenance: dict | None = None,
    candidates_dir: Path | None = None,
) -> dict:
    """Stage a candidate skill change. Never applies to live skills.

    Creates ``improvement/candidates/<candidate-id>/`` with:

    - ``skill.patch`` — the unified diff (the change).
    - ``linked-lessons.json`` — lesson IDs that motivated this change.
    - ``evaluation-summary.md`` — human-readable summary.
    - ``safety-checklist.md`` — to be filled by safety tests (initially
      a placeholder with the test names listed).
    - ``rollback.patch`` — the reverse diff (how to undo).
    - ``provenance.json`` — session, agent, model, timestamp.
    - ``metadata.json`` — the candidate metadata (same dict returned
      by this function, written to disk for later loading).

    Args:
        skill_path: repo-relative path to the skill being modified
            (e.g. ``skills/security/bounty-attack/SKILL.md``). May be
            absolute or relative. Stored as a POSIX-style relative path
            in the metadata.
        patch: the unified diff. Must be a string (not bytes).
        linked_lessons: lesson IDs that motivated this change. Stored
            as a JSON list.
        evaluation_summary: human-readable summary of the evaluation
            that produced this candidate. Markdown-formatted.
        provenance: dict with ``session``, ``agent``, ``model``,
            ``timestamp`` keys (any subset is fine; missing keys are
            filled with defaults). Used for auditability.
        candidates_dir: override the candidates directory (for
            testing). Defaults to ``<repo>/improvement/candidates/``.

    Returns:
        The candidate metadata dict::

            {
              "candidate_id": "<uuid4>",
              "staged_at": "<ISO 8601 UTC>",
              "skill_path": "skills/security/bounty-attack/SKILL.md",
              "patch_sha256": "<hex>",
              "linked_lessons": [...],
              "provenance": {...}
            }

    Never applies the patch to the live skill. The candidate stays
    staged until a human approves it (SI-029).
    """
    candidate_id = str(uuid.uuid4())
    staged_at = _now_iso()
    patch_sha256 = _sha256_text(patch)

    # Normalize the skill path to a repo-relative POSIX string.
    skill_path_str = str(skill_path).strip()
    while skill_path_str.startswith("./"):
        skill_path_str = skill_path_str[2:]

    # Resolve candidates_dir.
    if candidates_dir is None:
        anchor = Path(skill_path).resolve() if Path(skill_path).is_absolute() else Path.cwd()
        repo_root = _repo_root_from(anchor)
        candidates_dir = repo_root / CANDIDATES_DIR
    candidates_dir = Path(candidates_dir)
    candidate_dir = candidates_dir / candidate_id
    candidate_dir.mkdir(parents=True, exist_ok=True)

    # Provenance with defaults.
    prov = provenance or {}
    prov_full = {
        "session": prov.get("session", ""),
        "agent": prov.get("agent", ""),
        "model": prov.get("model", ""),
        "timestamp": prov.get("timestamp", staged_at),
    }

    metadata = {
        "candidate_id": candidate_id,
        "staged_at": staged_at,
        "skill_path": skill_path_str,
        "patch_sha256": patch_sha256,
        "linked_lessons": list(linked_lessons),
        "provenance": prov_full,
    }

    # Write artifacts.
    (candidate_dir / "skill.patch").write_text(patch, encoding="utf-8")
    (candidate_dir / "linked-lessons.json").write_text(
        json.dumps(list(linked_lessons), indent=2) + "\n", encoding="utf-8"
    )
    summary_text = evaluation_summary
    if summary_text and not summary_text.endswith("\n"):
        summary_text += "\n"
    (candidate_dir / "evaluation-summary.md").write_text(summary_text, encoding="utf-8")
    (candidate_dir / "rollback.patch").write_text(_reverse_patch(patch), encoding="utf-8")
    (candidate_dir / "provenance.json").write_text(
        json.dumps(prov_full, indent=2, sort_keys=True) + "\n", encoding="utf-8"
    )
    (candidate_dir / "metadata.json").write_text(
        json.dumps(metadata, indent=2, sort_keys=True) + "\n", encoding="utf-8"
    )

    # Safety-checklist placeholder. run_safety_tests() will overwrite
    # this with the actual test results.
    checklist_lines = [
        "# Safety checklist — candidate " + candidate_id,
        "",
        "Staged at: " + staged_at,
        "Skill: " + skill_path_str,
        "",
        "## Safety tests (run by labimprove.run_safety_tests)",
        "",
        "- [ ] MUT-001: patch only modifies allowlisted files",
        "- [ ] MUT-002: patch does not touch safety-critical paths",
        "- [ ] SIZE-001: complexity under ceiling",
        "- [ ] SIZE-002: diff size under limit",
        "- [ ] SIZE-003: skill length under limit",
        "- [ ] LEAK-001: no private label access",
        "",
        "Status: PENDING (run_safety_tests has not been called yet).",
    ]
    (candidate_dir / "safety-checklist.md").write_text(
        "\n".join(checklist_lines) + "\n", encoding="utf-8"
    )

    return metadata


